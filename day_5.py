def get_my_seat(b_passes: list) -> int:
    seats_grid = [list(range(0, 8)) for _ in range(0, 128)]

    for boarding_pass in b_passes:
        row = get_seat_row(boarding_pass=boarding_pass)
        column = get_seat_column(boarding_pass=boarding_pass)
        # Plot the seat in a grid
        seats_grid[row][column] = "💺"

    exit = False
    for column_index, seat_row in enumerate(seats_grid):
        if exit:
            break
        if "💺" not in seat_row:
            # Discard this seat row no guys are here
            print(f"Discarded {seat_row}")
            continue
        for seat_column in seat_row:
            if seat_column != "💺":
                # If we find a seat that does not have a seat on the grid then it's empty and it's ours
                print(seat_row, seat_column)
                seat_id = (column_index * 8) + seat_column
                exit = True
                break
    return seats_grid, seat_id


