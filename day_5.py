def get_seat_row(boarding_pass: str) -> int:
    row_chars = boarding_pass[:7]
    current_selection = list(range(128))

    for c in row_chars:
        if c == "F":
            half_index = len(current_selection) // 2
            current_selection = current_selection[:half_index]
        elif c == "B":
            half_index = len(current_selection) // 2
            current_selection = current_selection[half_index:]
    return current_selection[0]


def get_seat_column(boarding_pass: str) -> int:
    row_chars = boarding_pass[7:]
    current_selection = list(range(8))

    for c in row_chars:
        if c == "R":
            half_index = len(current_selection) // 2
            current_selection = current_selection[half_index:]
        elif c == "L":
            half_index = len(current_selection) // 2
            current_selection = current_selection[:half_index]
    return current_selection[0]


def get_max_id(b_passes: list) -> int:
    seat_ids = []

    for boarding_pass in b_passes:
        row = get_seat_row(boarding_pass=boarding_pass)
        column = get_seat_column(boarding_pass=boarding_pass)
        seat_id = (row * 8) + column
        seat_ids.append(seat_id)
    return max(seat_ids)


